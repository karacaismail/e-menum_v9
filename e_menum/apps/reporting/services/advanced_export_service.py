    def export_to_bi_format(
        self,
        report_data: dict,
        bi_tool: str,
    ) -> bytes:
        """
        Export report data in a BI tool-compatible format.

        Currently provides stubs for Power BI and Tableau formats.
        The actual format implementation requires the respective
        SDK/libraries which are not yet integrated.

        For now, exports data in an intermediate JSON format that
        can be imported by BI tools via their data connectors.

        Args:
            report_data: The report result data
            bi_tool: BI tool name ('powerbi' or 'tableau')

        Returns:
            bytes: File content in the BI tool's format

        Raises:
            ValueError: If the BI tool is not supported
        """
        bi_tool_lower = bi_tool.lower().strip()

        if bi_tool_lower == 'powerbi':
            return self._export_powerbi_format(report_data)
        elif bi_tool_lower == 'tableau':
            return self._export_tableau_format(report_data)
        else:
            raise ValueError(
                f'Unsupported BI tool: {bi_tool}. '
                f'Supported tools: powerbi, tableau'
            )

    def _export_powerbi_format(self, report_data: dict) -> bytes:
        """
        Export data in Power BI compatible JSON format.

        The output is a structured JSON file that can be imported
        into Power BI Desktop using the JSON data connector or
        used with Power BI REST API for dataset creation.

        Args:
            report_data: The report result data

        Returns:
            bytes: JSON content formatted for Power BI import
        """
        rows = self._extract_rows(report_data)

        # Power BI compatible structure
        pbi_data = {
            'name': report_data.get('title', 'E-Menum Report'),
            'tables': [
                {
                    'name': 'ReportData',
                    'columns': [],
                    'rows': [],
                },
            ],
            'metadata': {
                'source': 'E-Menum Analytics Platform',
                'generated_at': timezone.now().isoformat(),
                'format_version': '1.0',
                'bi_tool': 'powerbi',
            },
        }

        if rows:
            # Infer column types
            headers = list(rows[0].keys())
            for header in headers:
                sample = rows[0].get(header)
                col_type = self._infer_bi_type(sample)
                pbi_data['tables'][0]['columns'].append({
                    'name': header,
                    'dataType': col_type,
                })

            # Add rows
            for row in rows:
                pbi_row = {}
                for header in headers:
                    value = row.get(header)
                    pbi_row[header] = self._format_value(value)
                pbi_data['tables'][0]['rows'].append(pbi_row)

        # Add summary as a separate table
        summary = report_data.get('summary', {})
        if summary:
            summary_table = {
                'name': 'Summary',
                'columns': [
                    {'name': 'Metric', 'dataType': 'string'},
                    {'name': 'Value', 'dataType': 'string'},
                ],
                'rows': [
                    {'Metric': str(k), 'Value': str(self._format_value(v))}
                    for k, v in summary.items()
                ],
            }
            pbi_data['tables'].append(summary_table)

        content = json.dumps(pbi_data, indent=2, ensure_ascii=False, default=str)
        return content.encode('utf-8')

    def _export_tableau_format(self, report_data: dict) -> bytes:
        """
        Export data in Tableau compatible JSON/CSV format.

        The output is a structured JSON file that can be imported
        into Tableau Desktop using the JSON file connector.

        Args:
            report_data: The report result data

        Returns:
            bytes: JSON content formatted for Tableau import
        """
        rows = self._extract_rows(report_data)

        tableau_data = {
            'metadata': {
                'source': 'E-Menum Analytics Platform',
                'generated_at': timezone.now().isoformat(),
                'format_version': '1.0',
                'bi_tool': 'tableau',
                'title': report_data.get('title', 'E-Menum Report'),
            },
            'schema': {},
            'data': [],
        }

        if rows:
            # Build schema
            headers = list(rows[0].keys())
            for header in headers:
                sample = rows[0].get(header)
                tableau_data['schema'][header] = {
                    'dataType': self._infer_bi_type(sample),
                    'role': self._infer_tableau_role(header),
                }

            # Add flattened rows
            for row in rows:
                tableau_row = {}
                for header in headers:
                    value = row.get(header)
                    tableau_row[header] = self._format_value(value)
                tableau_data['data'].append(tableau_row)

        content = json.dumps(tableau_data, indent=2, ensure_ascii=False, default=str)
        return content.encode('utf-8')

