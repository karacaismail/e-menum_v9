    def build_custom_report(
        self,
        org_id,
        sections: List[Dict[str, Any]],
        parameters: Optional[Dict[str, Any]] = None,
    ) -> dict:
        """
        Build a custom report composed of multiple sections.

        Each section references a report handler type and parameters.
        The builder aggregates results from multiple handlers into
        a single composite report.

        Args:
            org_id: Organization UUID (multi-tenant isolation)
            sections: List of section definitions:
                - type (str): Handler type key or predefined section
                - parameters (dict): Section-specific parameters
                - title (str, optional): Section title
            parameters: Global report parameters:
                - title (str): Overall report title
                - description (str): Report description

        Returns:
            dict: Composite report with:
                - title (str)
                - description (str)
                - generated_at (str)
                - sections (list): Section results
                - summary (dict): Aggregated summary
        """
        from apps.reporting.services.report_engine import ReportEngine

        parameters = parameters or {}
        engine = ReportEngine()

        report = {
            'title': parameters.get('title', 'Custom Report'),
            'description': parameters.get('description', ''),
            'generated_at': timezone.now().isoformat(),
            'sections': [],
            'summary': {},
        }

        for idx, section_def in enumerate(sections):
            section_type = section_def.get('type', '')
            section_params = section_def.get('parameters', {})
            section_title = section_def.get('title', f'Section {idx + 1}')

            section_result = {
                'index': idx,
                'type': section_type,
                'title': section_title,
                'status': 'pending',
                'data': None,
                'error': None,
            }

            try:
                # Try to execute as a registered report handler
                if engine.registry.is_registered(section_type):
                    execution = engine.execute_report(
                        org_id=str(org_id),
                        feature_key=section_type,
                        parameters=section_params,
                    )
                    section_result['data'] = execution.result_data
                    section_result['status'] = 'completed'

                    logger.info(
                        'Custom report section completed: org=%s type=%s',
                        org_id, section_type,
                    )
                else:
                    # Try built-in section types
                    section_data = self._build_builtin_section(
                        org_id=org_id,
                        section_type=section_type,
                        parameters=section_params,
                    )
                    if section_data is not None:
                        section_result['data'] = section_data
                        section_result['status'] = 'completed'
                    else:
                        section_result['status'] = 'skipped'
                        section_result['error'] = (
                            f'Unknown section type: {section_type}'
                        )

            except Exception as exc:
                section_result['status'] = 'failed'
                section_result['error'] = str(exc)
                logger.error(
                    'Custom report section failed: org=%s type=%s error=%s',
                    org_id, section_type, exc,
                )

            report['sections'].append(section_result)

        # Build aggregated summary
        completed_count = sum(
            1 for s in report['sections'] if s['status'] == 'completed'
        )
        failed_count = sum(
            1 for s in report['sections'] if s['status'] == 'failed'
        )
        report['summary'] = {
            'total_sections': len(sections),
            'completed': completed_count,
            'failed': failed_count,
            'skipped': len(sections) - completed_count - failed_count,
        }

        return report

    def _build_builtin_section(
        self,
        org_id,
        section_type: str,
        parameters: dict,
    ) -> Optional[dict]:
        """
        Build a built-in section type that does not require a handler.

        Args:
            org_id: Organization UUID
            section_type: Section type identifier
            parameters: Section parameters

        Returns:
            dict: Section data, or None if type is unknown
        """
        builtin_handlers = {
            'credit_summary': self._build_credit_summary_section,
            'benchmark_comparison': self._build_benchmark_section,
        }

        handler = builtin_handlers.get(section_type)
        if handler:
            return handler(org_id, parameters)
        return None

    @staticmethod
    def _build_credit_summary_section(org_id, parameters: dict) -> dict:
        """Build a credit usage summary section."""
        from apps.reporting.services.credit_service import CreditService

        service = CreditService()
        return service.get_usage_summary(
            org_id=org_id,
            start_date=parameters.get('start_date'),
            end_date=parameters.get('end_date'),
        )

    @staticmethod
    def _build_benchmark_section(org_id, parameters: dict) -> dict:
        """Build a benchmark comparison section."""
        from apps.reporting.services.benchmark_service import BenchmarkService

        service = BenchmarkService()
        metric_name = parameters.get('metric_name', 'daily_revenue')
        period = parameters.get('period', 'MONTHLY')

        return service.compare_org_to_benchmark(
            org_id=org_id,
            metric_name=metric_name,
            period=period,
        )

    # ─────────────────────────────────────────────────────────
    # HELPERS
    # ─────────────────────────────────────────────────────────

    @staticmethod
    def _extract_rows(report_data: dict) -> List[dict]:
        """Extract tabular rows from report data."""
        for key in ('rows', 'data', 'items', 'results', 'products', 'orders'):
            if key in report_data and isinstance(report_data[key], list):
                rows = report_data[key]
                if rows and isinstance(rows[0], dict):
                    return rows
        return []

    @staticmethod
    def _format_value(value: Any) -> Any:
        """Format a value for display in exports."""
        if isinstance(value, Decimal):
            return float(value)
        if isinstance(value, datetime):
            return value.strftime('%Y-%m-%d %H:%M')
        if isinstance(value, (dict, list)):
            return str(value)
        return value

    @staticmethod
    def _infer_bi_type(value: Any) -> str:
        """Infer BI tool data type from a Python value."""
        if isinstance(value, bool):
            return 'boolean'
        if isinstance(value, int):
            return 'int64'
        if isinstance(value, (float, Decimal)):
            return 'double'
        if isinstance(value, datetime):
            return 'dateTime'
        return 'string'

    @staticmethod
    def _infer_tableau_role(field_name: str) -> str:
        """Infer Tableau field role from field name."""
        measure_keywords = [
            'amount', 'total', 'count', 'revenue', 'cost', 'price',
            'quantity', 'avg', 'sum', 'percent', 'rate', 'value',
        ]
        name_lower = field_name.lower()
        if any(kw in name_lower for kw in measure_keywords):
            return 'measure'
        return 'dimension'
