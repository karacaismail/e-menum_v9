"""
Advanced Export Service for the Reporting module.

Provides enhanced export capabilities beyond the basic ExportService:
    - White-label PDF with organization branding (logo, colors, fonts)
    - BI tool format stubs (Power BI, Tableau)
    - Custom multi-section report builder

Usage:
    from apps.reporting.services.advanced_export_service import AdvancedExportService

    service = AdvancedExportService()

    # White-label PDF
    pdf_bytes = service.export_white_label_pdf(
        report_data=execution.result_data,
        branding={
            'logo_url': 'https://example.com/logo.png',
            'primary_color': '#2563EB',
            'company_name': 'Cafe Istanbul',
            'font_family': 'Helvetica',
        },
    )

    # BI tool export
    pbix_bytes = service.export_to_bi_format(report_data, bi_tool='powerbi')

    # Custom multi-section report
    report = service.build_custom_report(
        org_id=org.id,
        sections=[
            {'type': 'revenue_summary', 'parameters': {'period': 'MONTHLY'}},
            {'type': 'top_products', 'parameters': {'limit': 10}},
            {'type': 'customer_trends', 'parameters': {'days': 30}},
        ],
        parameters={'title': 'Monthly Business Review'},
    )

Critical Rules:
    - EVERY query MUST filter by organization_id (multi-tenant isolation)
"""

import io
import json
import logging
from datetime import datetime
from decimal import Decimal
from typing import Any, Dict, List, Optional

from django.utils import timezone

logger = logging.getLogger(__name__)


class AdvancedExportService:
    """
    Advanced export capabilities for the reporting module.

    Extends basic export with branding, BI tool formats, and
    custom multi-section report building.
    """

    # ─────────────────────────────────────────────────────────
    # WHITE-LABEL PDF EXPORT
    # ─────────────────────────────────────────────────────────

    def export_white_label_pdf(
        self,
        report_data: dict,
        branding: Optional[Dict[str, Any]] = None,
    ) -> bytes:
        """
        Generate a white-label PDF with organization branding.

        Creates a professional PDF report that includes the organization's
        logo, brand colors, and custom fonts.

        Args:
            report_data: The report result data
            branding: Branding configuration:
                - logo_url (str): URL to organization logo
                - primary_color (str): Hex color code (e.g., '#2563EB')
                - secondary_color (str): Hex color code
                - company_name (str): Organization display name
                - font_family (str): Font family name
                - footer_text (str): Custom footer text
                - header_text (str): Custom header text

        Returns:
            bytes: PDF file content
        """
        try:
            from reportlab.lib import colors
            from reportlab.lib.pagesizes import A4
            from reportlab.lib.styles import ParagraphStyle, getSampleStyleSheet
            from reportlab.lib.units import cm, mm
            from reportlab.platypus import (
                Image,
                Paragraph,
                SimpleDocTemplate,
                Spacer,
                Table,
                TableStyle,
            )
        except ImportError:
            logger.error(
                'reportlab is required for PDF export. '
                'Install with: pip install reportlab'
            )
            raise ImportError('reportlab is required for white-label PDF export')

        branding = branding or {}
        primary_color = branding.get('primary_color', '#2563EB')
        secondary_color = branding.get('secondary_color', '#1E40AF')
        company_name = branding.get('company_name', 'E-Menum')
        font_family = branding.get('font_family', 'Helvetica')
        footer_text = branding.get('footer_text', f'Generated by {company_name}')
        header_text = branding.get('header_text', '')

        # Convert hex colors to reportlab colors
        primary_rl = colors.HexColor(primary_color)
        secondary_rl = colors.HexColor(secondary_color)

        output = io.BytesIO()
        doc = SimpleDocTemplate(
            output,
            pagesize=A4,
            rightMargin=2 * cm,
            leftMargin=2 * cm,
            topMargin=2.5 * cm,
            bottomMargin=2 * cm,
        )

        styles = getSampleStyleSheet()
        elements = []

        # ── Logo / Company Header ──
        if header_text:
            header_style = ParagraphStyle(
                'BrandHeader',
                parent=styles['Normal'],
                fontSize=10,
                textColor=colors.grey,
            )
            elements.append(Paragraph(header_text, header_style))
            elements.append(Spacer(1, 4))

        company_style = ParagraphStyle(
            'CompanyName',
            parent=styles['Title'],
            fontSize=20,
            textColor=primary_rl,
            fontName=font_family,
            spaceAfter=4,
        )
        elements.append(Paragraph(company_name, company_style))

        # ── Report Title ──
        title = report_data.get('title', 'Report')
        title_style = ParagraphStyle(
            'ReportTitle',
            parent=styles['Heading1'],
            fontSize=16,
            textColor=secondary_rl,
            fontName=font_family,
            spaceAfter=8,
        )
        elements.append(Paragraph(title, title_style))

        # ── Generation Date ──
        date_style = ParagraphStyle(
            'DateLine',
            parent=styles['Normal'],
            fontSize=9,
            textColor=colors.grey,
        )
        generated_at = timezone.now().strftime('%Y-%m-%d %H:%M')
        elements.append(Paragraph(f'Generated: {generated_at}', date_style))
        elements.append(Spacer(1, 16))

        # ── Divider Line ──
        divider_table = Table(
            [['']],
            colWidths=[16 * cm],
            rowHeights=[2],
        )
        divider_table.setStyle(TableStyle([
            ('BACKGROUND', (0, 0), (-1, -1), primary_rl),
            ('LINEBELOW', (0, 0), (-1, -1), 0, primary_rl),
        ]))
        elements.append(divider_table)
        elements.append(Spacer(1, 12))

        # ── Summary Section ──
        summary = report_data.get('summary', {})
        if summary:
            summary_title = ParagraphStyle(
                'SummaryTitle',
                parent=styles['Heading2'],
                textColor=primary_rl,
            )
            elements.append(Paragraph('Summary', summary_title))

            summary_data = [
                [str(k), str(self._format_value(v))]
                for k, v in summary.items()
            ]
            if summary_data:
                t = Table(summary_data, colWidths=[8 * cm, 8 * cm])
                t.setStyle(TableStyle([
                    ('FONTNAME', (0, 0), (0, -1), f'{font_family}-Bold'
                     if font_family == 'Helvetica' else font_family),
                    ('GRID', (0, 0), (-1, -1), 0.5, colors.HexColor('#E5E7EB')),
                    ('VALIGN', (0, 0), (-1, -1), 'MIDDLE'),
                    ('BOTTOMPADDING', (0, 0), (-1, -1), 6),
                    ('TOPPADDING', (0, 0), (-1, -1), 6),
                    ('ROWBACKGROUNDS', (0, 0), (-1, -1),
                     [colors.white, colors.HexColor('#F9FAFB')]),
                ]))
                elements.append(t)
                elements.append(Spacer(1, 16))

        # ── Data Table ──
        rows = self._extract_rows(report_data)
        if rows:
            data_title = ParagraphStyle(
                'DataTitle',
                parent=styles['Heading2'],
                textColor=primary_rl,
            )
            elements.append(Paragraph('Data', data_title))

            headers = list(rows[0].keys())
            table_data = [headers]
            for row in rows[:100]:
                table_data.append([
                    str(self._format_value(row.get(h, '')))
                    for h in headers
                ])

            num_cols = len(headers)
            col_width = min(16 * cm / max(num_cols, 1), 5 * cm)

            t = Table(table_data, colWidths=[col_width] * num_cols)
            t.setStyle(TableStyle([
                ('FONTNAME', (0, 0), (-1, 0), f'{font_family}-Bold'
                 if font_family == 'Helvetica' else font_family),
                ('BACKGROUND', (0, 0), (-1, 0), primary_rl),
                ('TEXTCOLOR', (0, 0), (-1, 0), colors.white),
                ('ALIGN', (0, 0), (-1, -1), 'LEFT'),
                ('FONTSIZE', (0, 0), (-1, -1), 8),
                ('GRID', (0, 0), (-1, -1), 0.5, colors.HexColor('#E5E7EB')),
                ('ROWBACKGROUNDS', (0, 1), (-1, -1),
                 [colors.white, colors.HexColor('#F9FAFB')]),
                ('BOTTOMPADDING', (0, 0), (-1, -1), 4),
                ('TOPPADDING', (0, 0), (-1, -1), 4),
            ]))
            elements.append(t)

            if len(rows) > 100:
                elements.append(Spacer(1, 6))
                elements.append(Paragraph(
                    f'Showing 100 of {len(rows)} rows.',
                    styles['Italic'],
                ))

        # ── Footer ──
        elements.append(Spacer(1, 24))
        footer_style = ParagraphStyle(
            'Footer',
            parent=styles['Normal'],
            fontSize=8,
            textColor=colors.grey,
            alignment=1,  # Center
        )
        elements.append(Paragraph(footer_text, footer_style))

        doc.build(elements)
        return output.getvalue()

